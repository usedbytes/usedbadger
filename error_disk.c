void init_error_filesystem(const struct fat_ramdisk *disk, const char *error)
{
	memset(disk->data, 0, disk->num_sectors * disk->sector_size);

	// Blit in the template
	memcpy(disk->data, &error_disk[0][0], sizeof(error_disk));

	// Update the filesystem size
	uint16_t num_sectors = disk->num_sectors;
	uint8_t *dst_num_sectors = &disk->data[BOOT_SECTOR_NUM_SECTORS_OFFSET];
	memcpy(dst_num_sectors, &num_sectors, sizeof(num_sectors));

	// Update the error file
	char *dst_contents = &disk->data[ERROR_CONTENTS_SECTOR * ERROR_DISK_SECTOR_SIZE];
	snprintf(dst_contents, ERROR_DISK_SECTOR_SIZE,
			"Encountered error setting up filesystem: %s\n", error);

	// Update the error file size
	uint32_t file_size = strnlen(dst_contents, ERROR_DISK_SECTOR_SIZE);
	uint8_t *dst_size = &disk->data[ERROR_SIZE_SECTOR * ERROR_DISK_SECTOR_SIZE + ERROR_SIZE_OFFSET];
	memcpy(dst_size, &file_size, sizeof(file_size));

}
